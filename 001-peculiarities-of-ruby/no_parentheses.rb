# Parentheses are optional (except when required :) )

def say(text)
  p(text)
end

say('hello')

# You can do the same without any parentheses

def hello d
  'hello ' + d
end

hello 'to me'

# You can write sentences

say hello 'to me'
# => "hello to me"


